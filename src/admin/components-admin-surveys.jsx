function RichTextEditor({ value, onChange, placeholder = "Question text" }) {
  const editorRef = useRef(null);
  const [focused, setFocused] = useState(false);
  const [formats, setFormats] = useState({
    bold: false,
    italic: false,
    insertUnorderedList: false,
    insertOrderedList: false,
  });

  const normalizedValue = normalizeRichTextHtml(value);

  useEffect(() => {
    const el = editorRef.current;
    if (!el) return;

    if (el.innerHTML !== normalizedValue) {
      el.innerHTML = normalizedValue || "";
    }
  }, [normalizedValue]);

  function updateFormats() {
    try {
      setFormats({
        bold: !!document.queryCommandState("bold"),
        italic: !!document.queryCommandState("italic"),
        insertUnorderedList: !!document.queryCommandState("insertUnorderedList"),
        insertOrderedList: !!document.queryCommandState("insertOrderedList"),
      });
    } catch {
      setFormats({
        bold: false,
        italic: false,
        insertUnorderedList: false,
        insertOrderedList: false,
      });
    }
  }

  function emitChange() {
    const el = editorRef.current;
    if (!el) return;

    const rawHtml = el.innerHTML || "";
    const textOnly = stripHtmlForEmptyCheck(rawHtml);

    if (!textOnly) {
      onChange("");
      return;
    }

    onChange(rawHtml);
  }

  function runCommand(command, commandValue = null) {
    const el = editorRef.current;
    if (!el) return;

    el.focus();

    try {
      document.execCommand(command, false, commandValue);
    } catch {}

    if (command === "formatBlock") {
      try {
        document.execCommand("defaultParagraphSeparator", false, "p");
      } catch {}
    }

    updateFormats();
    emitChange();
  }

  function handleInput() {
    emitChange();
    updateFormats();
  }

  function handlePaste(e) {
    e.preventDefault();
    const text = e.clipboardData?.getData("text/plain") || "";
    try {
      document.execCommand("insertText", false, text);
    } catch {
      const el = editorRef.current;
      if (!el) return;
      el.innerText = `${el.innerText || ""}${text}`;
    }
    emitChange();
    updateFormats();
  }

  const isEmpty = !stripHtmlForEmptyCheck(normalizedValue);

  return (
    <div
      style={{
        border: `1px solid ${focused ? "#4f46e5" : "#d1d5db"}`,
        borderRadius: 10,
        overflow: "hidden",
        background: "#fff",
        boxShadow: focused ? "0 0 0 3px rgba(79,70,229,0.10)" : "none",
      }}
    >
      <div
        style={{
          display: "flex",
          gap: 6,
          alignItems: "center",
          padding: 8,
          borderBottom: "1px solid #e5e7eb",
          background: "#fafafa",
          flexWrap: "wrap",
        }}
      >
        <RichToolbarButton
          title="Bold"
          active={formats.bold}
          onMouseDown={(e) => {
            e.preventDefault();
            runCommand("bold");
          }}
        >
          <BoldIcon size={14} />
        </RichToolbarButton>

        <RichToolbarButton
          title="Italic"
          active={formats.italic}
          onMouseDown={(e) => {
            e.preventDefault();
            runCommand("italic");
          }}
        >
          <ItalicIcon size={14} />
        </RichToolbarButton>

        <RichToolbarButton
          title="Bullet list"
          active={formats.insertUnorderedList}
          onMouseDown={(e) => {
            e.preventDefault();
            runCommand("insertUnorderedList");
          }}
        >
          <ListIcon size={14} />
        </RichToolbarButton>

        <RichToolbarButton
          title="Numbered list"
          active={formats.insertOrderedList}
          onMouseDown={(e) => {
            e.preventDefault();
            runCommand("insertOrderedList");
          }}
        >
          <OrderedListIcon size={14} />
        </RichToolbarButton>

        <RichToolbarButton
          title="Paragraph"
          onMouseDown={(e) => {
            e.preventDefault();
            runCommand("formatBlock", "p");
          }}
        >
          <span style={{ fontSize: 11, fontWeight: 700 }}>P</span>
        </RichToolbarButton>
      </div>

      <div style={{ position: "relative" }}>
        {isEmpty && !focused && (
          <div
            style={{
              position: "absolute",
              left: 12,
              top: 12,
              color: "#9ca3af",
              pointerEvents: "none",
              fontSize: 14,
            }}
          >
            {placeholder}
          </div>
        )}

        <div
          ref={editorRef}
          contentEditable
          suppressContentEditableWarning
          onFocus={() => {
            setFocused(true);
            updateFormats();
          }}
          onBlur={() => {
            setFocused(false);
            emitChange();
          }}
          onInput={handleInput}
          onKeyUp={updateFormats}
          onMouseUp={updateFormats}
          onPaste={handlePaste}
          style={{
            minHeight: 120,
            padding: 12,
            outline: "none",
            lineHeight: 1.5,
            fontSize: 14,
          }}
        />
      </div>
    </div>
  );
}

