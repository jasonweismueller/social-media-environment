function makeEditorId() {
  return `editor_${Date.now()}_${Math.random().toString(36).slice(2, 9)}`;
}

function clampInt(value, min, max, fallback) {
  const n = Number(value);
  if (!Number.isFinite(n)) return fallback;
  return Math.max(min, Math.min(max, Math.round(n)));
}

function normalizeLinkedFeedIds(input) {
  return Array.isArray(input) ? input.map(String).filter(Boolean) : [];
}

function makeSequentialValue(prefix, index) {
  return `${prefix}_${index + 1}`;
}

function makeNumericValue(index) {
  return String(index + 1);
}

function sanitizeQuestionId(value, fallback = "") {
  const cleaned = String(value ?? "")
    .trim()
    .toUpperCase()
    .replace(/\s+/g, "_")
    .replace(/[^A-Z0-9_]/g, "");
  return cleaned || fallback;
}

function sanitizeFreeformValue(value, fallback = "") {
  const cleaned = String(value ?? "")
    .trim()
    .replace(/\s+/g, "_")
    .replace(/[^\w-]/g, "");
  return cleaned || fallback;
}

function preserveEmptyOrSanitize(value, fallback = "") {
  if (value === "") return "";
  if (value === null || value === undefined) return fallback;
  return sanitizeFreeformValue(value, fallback);
}

function isQuestionIdAutoGenerated(value = "") {
  return /^(Q_\d+|Q_\d+_\d+|Q_\d{6,}|Q_[A-Z0-9]+|q_\d+|q_[A-Za-z0-9_-]+)$/.test(
    String(value || "").trim()
  );
}

function makeMatrixRowValue(questionId, index) {
  const base = sanitizeQuestionId(questionId);
  return base ? `${base}_${index + 1}` : makeSequentialValue("row", index);
}

function shouldAutoRewriteRowValues(question) {
  const type = question?.type;
  return (
    type === SURVEY_QUESTION_TYPES.MATRIX_SINGLE ||
    type === SURVEY_QUESTION_TYPES.MATRIX_MULTI ||
    type === SURVEY_QUESTION_TYPES.BIPOLAR
  );
}

function ensureChoiceArray(items = []) {
  return (Array.isArray(items) ? items : []).map((item, i) => ({
    value: preserveEmptyOrSanitize(item?.value, makeNumericValue(i)),
    label: String(item?.label ?? ""),
  }));
}

function ensureMatrixArray(items = [], prefix = "item") {
  const useNumericDefault = prefix === "col";

  return (Array.isArray(items) ? items : []).map((item, i) => ({
    value: preserveEmptyOrSanitize(
      item?.value,
      useNumericDefault ? makeNumericValue(i) : makeSequentialValue(prefix, i)
    ),
    label: String(item?.label ?? ""),
  }));
}

function ensureMatrixRowsFromQuestionId(items = [], questionId = "") {
  return (Array.isArray(items) ? items : []).map((item, i) => ({
    value: preserveEmptyOrSanitize(item?.value, makeMatrixRowValue(questionId, i)),
    label: String(item?.label ?? ""),
  }));
}

function ensureBipolarRowArray(items = [], questionId = "") {
  return (Array.isArray(items) ? items : []).map((item, i) => ({
    value: preserveEmptyOrSanitize(item?.value, makeMatrixRowValue(questionId, i)),
    left_label: String(item?.left_label ?? item?.label ?? "").trim(),
    right_label: String(item?.right_label ?? "").trim(),
  }));
}

function rewriteQuestionRowValues(question, nextQuestionId) {
  if (!shouldAutoRewriteRowValues(question)) return question;

  const nextId = sanitizeQuestionId(nextQuestionId, question?.id || "");
  if (!nextId) return question;

  return {
    ...question,
    rows: (Array.isArray(question?.rows) ? question.rows : []).map((row, i) => ({
      ...row,
      value: makeMatrixRowValue(nextId, i),
    })),
  };
}

function makeCopiedQuestionId(existingIds = [], sourceId = "") {
  const cleanSourceId = sanitizeQuestionId(sourceId, `Q_${Date.now()}`);
  const idSet = new Set((Array.isArray(existingIds) ? existingIds : []).map((x) => String(x || "").trim()));

  let nextId = `${cleanSourceId}_COPY`;
  if (!idSet.has(nextId)) return nextId;

  let counter = 2;
  while (idSet.has(`${cleanSourceId}_COPY_${counter}`)) {
    counter += 1;
  }

  return `${cleanSourceId}_COPY_${counter}`;
}

function reorderArray(list = [], fromIndex, toIndex) {
  const arr = [...list];
  if (
    fromIndex === toIndex ||
    fromIndex < 0 ||
    toIndex < 0 ||
    fromIndex >= arr.length ||
    toIndex >= arr.length
  ) {
    return arr;
  }

  const [moved] = arr.splice(fromIndex, 1);
  arr.splice(toIndex, 0, moved);
  return arr;
}

